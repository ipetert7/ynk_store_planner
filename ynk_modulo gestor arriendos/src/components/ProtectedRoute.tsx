'use client'

import { useSession } from 'next-auth/react'
import { useRouter } from 'next/navigation'
import { useEffect } from 'react'
import { UserRole, hasRoleLevel } from '@/lib/utils/permissions'
import LoadingSpinner from '@/components/ui/LoadingSpinner'

interface ProtectedRouteProps {
  children: React.ReactNode
  requiredRole?: UserRole
  fallback?: React.ReactNode
  redirectTo?: string
}

export default function ProtectedRoute({
  children,
  requiredRole = UserRole.ADMINISTRADOR,
  fallback,
  redirectTo = '/login',
}: ProtectedRouteProps) {
  const { data: session, status } = useSession()
  const router = useRouter()

  useEffect(() => {
    if (status === 'loading') {
      // Esperar a que NextAuth termine de cargar
      return
    }

    if (status === 'unauthenticated' || !session) {
      router.push(redirectTo)
      return
    }
  }, [status, session, router, redirectTo])

  // Mostrar loading mientras se carga la sesi칩n
  if (status === 'loading') {
    return (
      <div className="flex justify-center items-center h-64">
        <LoadingSpinner size="lg" text="Verificando permisos..." />
      </div>
    )
  }

  // Redireccionar si no est치 autenticado
  if (status === 'unauthenticated' || !session) {
    return null // El useEffect ya maneja la redirecci칩n
  }

  // Verificar permisos
  const userRole = session.user?.role as UserRole
  const hasPermission = hasRoleLevel(userRole, requiredRole)

  if (!hasPermission) {
    // Mostrar fallback personalizado o mensaje por defecto
    if (fallback) {
      return <>{fallback}</>
    }

    return (
      <div className="flex flex-col items-center justify-center h-64 text-center">
        <div className="mb-4">
          <svg
            className="mx-auto h-12 w-12 text-red-400"
            fill="none"
            stroke="currentColor"
            viewBox="0 0 24 24"
          >
            <path
              strokeLinecap="round"
              strokeLinejoin="round"
              strokeWidth={2}
              d="M12 9v2m0 4h.01m-6.938 4h13.856c1.54 0 2.502-1.667 1.732-2.5L13.732 4c-.77-.833-1.964-.833-2.732 0L4.082 16.5c-.77.833.192 2.5 1.732 2.5z"
            />
          </svg>
        </div>
        <h3 className="text-lg font-medium text-gray-900 mb-2">
          Acceso Denegado
        </h3>
        <p className="text-gray-600 mb-4">
          No tienes permisos suficientes para acceder a esta secci칩n.
        </p>
        <p className="text-sm text-gray-500">
          Rol requerido: <span className="font-medium">{requiredRole}</span> |
          Tu rol: <span className="font-medium">{userRole || 'Sin definir'}</span>
        </p>
      </div>
    )
  }

  // Usuario autorizado, mostrar contenido
  return <>{children}</>
}
